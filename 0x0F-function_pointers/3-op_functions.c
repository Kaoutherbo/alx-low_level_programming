#include "3-calc.h"

/**
 * op_add - function that calculate the sum of two numbers
 *
 * @a: an integer
 * @b: an integer
 * Return: sum of two numbers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that calculate the substraction of two numbers
 *
 * @a: an integer
 * @b: an integer
 * Return: difference of two numbers
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - function that calculate the product of two numbers
 *
 * @a: an integer
 * @b: an integer
 * Return: product of two numbers
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - function that calculate the division of two numbers
 *
 * @a: an integer
 * @b: an integer
 * Return: division of two numbers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that calculate the
 * remainder of the division of two numbers
 *
 * @a: an integer
 * @b: an integer
 * Return: the remainder of the division of two numbers
 */

int op_mod(int a, int b)
{
	return (a % b);
}

